/* Sorts an array of integers by bubbling larger numbers towards the end */
void bubble_sort(int arr[], int size);

// Sorts an array of integers by continuously moving the smallest unsorted 
// number to the first unsorted index.
void selection_sort(int arr[], int size);

// Sorts an array of integers by building the final sorted array one item at a
// time.
void insertion_sort(int arr[], int size);
